public class StateVariables {

	private int diskcounter = 0;
	private int whitecounter = 0;
	private int blackcounter = 0;

	private void increaseCounter() {
		diskcounter++;
	}

	private void increaseWhiteCounter() {
		whitecounter++;
	}

	private void increaseBlackCounter() {
		blackcounter++;
	}
}	
	
